package fr.iutvalence.hectorbarneoudfarisboulakhsoumi.othello;

/**
*  Enumeration which help us to return the pawns where a pawn is put
* @author Hector Barneoud / Faris Boulakhsoumi
*
*/

public enum Direction {
/**
 * The UP direction
 */
	UP,
	
	/**
	 * The DOWN direction
	 */
	DOWN,
	
	/**
	 * The RIGHT direction
	 */
	
	RIGHT,
	
	/**
	 * The LEFT direction
	 */
	
	LEFT,
	
	/**
	 * The UP_LEFT direction
	 */
	
	UP_LEFT,
	
	/**
	 * The UP_RIGHT direction
	 */
	
	UP_RIGHT,
	
	/**
	 * The DOWN_LEFT direction
	 */
	
	DOWN_LEFT,
	
	/**
	 * The DOWN_RIGHT direction
	 */
	DOWN_RIGHT,
	
	/**
	 * The NULL direction
	 */
	
	NULL;

}
